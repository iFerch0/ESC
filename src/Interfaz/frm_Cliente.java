 private void Llenarlista() {
        String Cabecera[] = {"Cedula", "Nombre", "Dirección", "Departamento", "Ciudad", "Telefono"};
        DefaultTableModel dtm = new DefaultTableModel(null, Cabecera);
        jTable1.setModel(dtm);
        try {
            LinkedList<Clientes> list = cl.Listar(frmSplash.con);
            Iterator ilist = list.iterator();
            String fila[] = new String[8];
            while (ilist.hasNext()) {
                Clientes cl = (Clientes) ilist.next();
                fila[0] = cl.getRUT();
                fila[1] = cl.getNombres();
                fila[2] = cl.getDireccion();
                fila[3] = cl.getDepartamento();
                fila[4] = cl.getCiudad();
                fila[5] = cl.getTelefono();
                dtm = (DefaultTableModel) jTable1.getModel();
                dtm.addRow(fila);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(95);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(250);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(95);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(160);
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(80);
}
 
 
  public void LimpiarCajas() {
        Funciones.LimpiarText(jPanel1);
        Funciones.Botones(jPanel2, false);
        botonNuevo.setEnabled(true);
        botonCancelar.setEnabled(true);
        Llenarlista();
        Activo = 0;
        txtCedula.grabFocus();
    }
