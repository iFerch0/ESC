    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        if (jTable1.getSelectedColumn() != -1) {
            txtCedula.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            txtNombre.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 1));
            txtDireccion.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 2));
            txtDepartamento.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 3));
            txtMunicipio.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 4));
            txtTelefono.setText((String) jTable1.getValueAt(jTable1.getSelectedRow(), 5));
            txtCedula.grabFocus();
            txtNombre.selectAll();
            txtCedula.setEnabled(false);
            Activo = 1;
            Funciones.PasarGrilla(evt, jTable1, botonGuardar, botonModificar, botonEliminar, Activo);
        }
    }//GEN-LAST:event_jTable1MousePressed

